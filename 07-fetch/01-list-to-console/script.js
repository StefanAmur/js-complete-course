(() => {
    fetch('../../_shared/api.json')
        .then(
            function (response) {
                if (response.status !== 200) {
                    console.log('Error: ' +
                        response.status);
                    return;
                }

                // Examine the text in the response
                response.json().then(function (data) {
                    console.log(data);
                });
            }
        )
        .catch(function (err) {
            console.log(err);
        });
})();
